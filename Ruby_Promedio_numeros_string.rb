#num_average method
def num_average(*texts)
  texts.each_with_index do |text, index|
    i = text[0..2]
    n = text[4..5].to_i
    m = text[10..11].to_i
    pp = text[7..8].to_i
    q = text[13..14].to_i

    texts[index] = [i,"-",((n+m)/2.0).round.to_s,".",((pp+q)/2.0).round.to_s].join
  end
  texts
end

