key = "Lets Code"
hash = {key: "The Future"}
hash2 = {key => "The Future"}


¿Cuál es la diferencia entre los dos hashes?

El la primera variable es una forma de declarar un variable tipo hash.
La forma de declarar la variable hash2 es con la palabra key como simbolo :key o como string 'key', de la siguiente forma:
hash2 = {:key => "The Future"}
hash2 = {"key" => "The Future"}

Si ambos variables hash se ingresan correctamente, ambas sera guardadas como
hash = {:key =>"The Future"}
hash2 = {:key => "The Future"}



